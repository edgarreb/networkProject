string codeName(string str1, string str2)
{
        string str3 = str1 + str2;
        str3.erase(remove(str3.begin(), str3.end(), ' '),str3.end());
        transform(str3.begin(), str3.end(), str3.begin(), ::tolower);

        return str3;
}
